// Token contract address
const tokenAddress = "0x73511669fd4dE447feD18BB79bAFeAC93aB7F31f"

// Account private key
const privateKey =
    "0xdf57089febbacf7ba0bc227dafbffa9fc08a93fdc68e1e42411a14efcf23656e"

// Provider URL for the localhost network
const providerURL = "http://localhost:8540"

// Create an instance of the provider
const provider = new ethers.providers.JsonRpcProvider(providerURL)

// Create a signer using the account's private key
const signer = new ethers.Wallet(privateKey, provider)

// Create an instance of the token contract
const tokenContract = new ethers.Contract(tokenAddress, tokenABI, signer)

// Function to call the 'mint' function
async function mintTokens(amount) {
    try {
        const tx = await tokenContract.connect(signer).mint(amount)
        await tx.wait()
        console.log("Tokens minted successfully!")
        console.log(tx)
    } catch (error) {
        console.error("Error minting tokens:", error)
    }
}

// Amount of tokens to mint
const amountToMint = ethers.utils.parseEther("100") // Example: Minting 100 tokens

// Call the mintTokens function with the desired amount
mintTokens(amountToMint)
